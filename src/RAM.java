/**
 * Interfaz que representa un modulo de memoria RAM.
 * Proporciona metodos para obtener la descripcion y el precio de la RAM.
 * 
 * @author Gabo, Pedro, Yamil, Isaac (Los Hijos de Korhal)
 */
public interface RAM {
    /**
     * Obtiene la descripcion del modulo de RAM.
     * @return La descripcion del modulo de RAM
     */
    String getDescription();

    /**
     * Obtiene el precio del modulo de RAM.
     * @return El precio del modulo de RAM
     */
    double getPrecio();
}
